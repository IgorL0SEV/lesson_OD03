# пояснение кода:
# left = list(filter(lambda i: i < element, s))
#
# 1. Функция filter
# filter(function, iterable) — это встроенная функция в Python, которая принимает две аргумента:
# function: функция, которая возвращает True или False для каждого элемента в iterable.
# iterable: любой итерируемый объект (например, список, кортеж и т.д.), элементы которого будут проверяться.
# 2. Лямбда-функция
# lambda i: i < element — это анонимная функция (лямбда-функция), которая принимает один аргумент i и возвращает True, если i меньше значения переменной element, и False в противном случае.
# 3. Параметр s
# s — это итерируемый объект (например, список), который мы хотим фильтровать. Каждый элемент из s будет передан в лямбда-функцию.
# 4. Результат
# Функция filter возвращает итератор, содержащий только те элементы из s, для которых лямбда-функция вернула True.
# Затем мы оборачиваем этот итератор в функцию list(), чтобы преобразовать его в список.

s = [1, 6, 7, 2, 3, 4, 5]
element = s[4]  # element = 3
left = list(filter(lambda i: i < element, s))
print(left)  # Вывод: [1, 2]
